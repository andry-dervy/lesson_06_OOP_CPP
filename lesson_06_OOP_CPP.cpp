int inputIntPositive()
{
  int n;
  while (true)
  {
    cin >> n;
    if (cin.fail())
    {
      cout << "Требуется ввести целое положительное число!" << endl;
      cin.clear();
      cin.ignore(32767, '\n');
    }
    else if ( n <= 0 )
    {
      cout << "Требуется ввести целое положительное число отличное от нуля!" << endl;
    }
    else
    {
      return n;
    }
  }
}


